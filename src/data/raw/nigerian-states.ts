export type State = keyof typeof data;
export type LGA = (typeof data)[keyof typeof data][number];
export type StateLGA<T extends State> = (typeof data)[T][number];

export function isState(state: string): state is State {
  return state in data;
}

export function isLGA(lga: string): lga is LGA {
  return Array.from(Object.values(data)).some(lgas => lgas.includes(lga as never));
}

export function isStateLGA<S extends State>(lga: string, state: S): lga is StateLGA<S> {
  return data[state].includes(lga as never);
}

